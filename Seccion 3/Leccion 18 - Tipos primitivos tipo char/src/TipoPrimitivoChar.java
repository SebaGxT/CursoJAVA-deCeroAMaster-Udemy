public class TipoPrimitivoChar {
    public static void main(String args[]){
        
        char miCaracter = 'a';
        System.out.println("miCaracter = " + miCaracter);
        
        //Codigo Unicode
        char varChar = '\u0021';
        System.out.println("varChar = " + varChar);
        
        //Decimal
        char varCharDecimal = 33;
        System.out.println("varCharDecimal = " + varCharDecimal);
        
        //Simbolo
        char varCharSimbolo = '!';
        System.out.println("varCharSimbolo = " + varCharSimbolo);
        
        var mi2Caracter = 'a';
        System.out.println("mi2Caracter = " + mi2Caracter);
        
        //Codigo Unicode
        var var2Char = '\u0021';
        System.out.println("var2Char = " + var2Char);
        
        //Decimal
        var var2CharDecimal = (char)33;
        System.out.println("var2CharDecimal = " + var2CharDecimal);
        
        //Simbolo
        var var2CharSimbolo = '!';
        System.out.println("var2CharSimbolo = " + var2CharSimbolo);
        
        int variableEnteraSimbolo = '!';
        System.out.println("variableEnteraSimbolo = " + variableEnteraSimbolo);
        
        int letra = 'a';
        System.out.println("letra = " + letra);
        
    }
}
